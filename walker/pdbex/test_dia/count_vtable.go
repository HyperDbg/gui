const (
	diaSDKPath    = `E:\Program Files\Microsoft Visual Studio\2022\BuildTools\DIA SDK`
	debuggersPath = `E:\Program Files\Windows Kits\10\Debuggers`
)

func copyFile(src, dst string) error {
	srcFile, err := os.Open(src)
	if err != nil {
		return err
	}
	defer srcFile.Close()

	dstFile, err := os.Create(dst)
	if err != nil {
		return err
	}
	defer dstFile.Close()

	_, err = io.Copy(dstFile, srcFile)
	return err
}

func copyDir(src, dst string) error {
	err := os.MkdirAll(dst, 0o755)
	if err != nil {
		return err
	}

	entries, err := os.ReadDir(src)
	if err != nil {
		return err
	}

	for _, entry := range entries {
		srcPath := filepath.Join(src, entry.Name())
		dstPath := filepath.Join(dst, entry.Name())

		if entry.IsDir() {
			if err := copyDir(srcPath, dstPath); err != nil {
				return err
			}
		} else {
			if err := copyFile(srcPath, dstPath); err != nil {
				return err
			}
			fmt.Printf("Copied: %s\n", dstPath)
		}
	}
	return nil
}

func syncDiaFiles() error {
	cwd, _ := os.Getwd()
	dstDir := filepath.Join(cwd, "walker", "pdbex", "test_dia", "dia")

	fmt.Printf("Copying DIA SDK from:\n  %s\n  to:\n  %s\n", diaSDKPath, dstDir)

	if err := copyDir(diaSDKPath, dstDir); err != nil {
		return err
	}

	symSrvFiles := []struct {
		src string
		dst string
	}{
		{filepath.Join(debuggersPath, "x64", "symsrv.dll"), filepath.Join(dstDir, "bin", "amd64", "symsrv.dll")},
		{filepath.Join(debuggersPath, "x86", "symsrv.dll"), filepath.Join(dstDir, "bin", "symsrv.dll")},
		{filepath.Join(debuggersPath, "arm64", "symsrv.dll"), filepath.Join(dstDir, "bin", "arm64", "symsrv.dll")},
	}

	fmt.Println("\nCopying symsrv.dll from Windows Kits:")
	for _, f := range symSrvFiles {
		if err := copyFile(f.src, f.dst); err != nil {
			fmt.Printf("Warning: failed to copy %s: %v\n", f.src, err)
		} else {
			fmt.Printf("Copied: %s\n", f.dst)
		}
	}

	return nil
}

func generateVtableForInterface(lines *[]string, methods *[]string, content string, interfaceName string, vtableStructName string) {
	inVtbl := false
	index := 0

	re := regexp.MustCompile(`\*\s*(\w+)\s*\)`)

	scanner := bufio.NewScanner(strings.NewReader(content))
	for scanner.Scan() {
		line := scanner.Text()

		if strings.Contains(line, "typedef struct "+vtableStructName) {
			inVtbl = true
			index = 0
			continue
		}

		if inVtbl && strings.Contains(line, "} "+vtableStructName+";") {
			break
		}

		if inVtbl {
			if strings.Contains(line, "BEGIN_INTERFACE") || strings.Contains(line, "END_INTERFACE") || strings.TrimSpace(line) == "" {
				continue
			}

			if strings.Contains(line, "STDMETHODCALLTYPE") {
				var methodName string

				if strings.Contains(line, "QueryInterface") {
					methodName = "QueryInterface"
				} else if strings.Contains(line, "AddRef") {
					methodName = "AddRef"
				} else if strings.Contains(line, "Release") {
					methodName = "Release"
				} else {
					matches := re.FindStringSubmatch(line)
					if len(matches) > 1 {
						methodName = matches[1]
					}
				}

				if methodName != "" {
					*methods = append(*methods, methodName)
					*lines = append(*lines, fmt.Sprintf("\tVT%s_%s VTableMethod = %d", interfaceName, methodName, index))
					index++
				}
			}
		}
	}
}

func generateVtableConstants(dia2Path string) error {
	content, err := os.ReadFile(dia2Path)
	if err != nil {
		return fmt.Errorf("failed to read dia2.h: %v", err)
	}
	dia2Content := string(content)

	var lines []string
	var allMethods []struct {
		iface   string
		methods []string
	}

	lines = append(lines, "// Code generated by test_dia/count_vtable.go. DO NOT EDIT.")
	lines = append(lines, "// Run: go run ./walker/pdbex/test_dia/count_vtable.go")
	lines = append(lines, "")
	lines = append(lines, "package pdbex")
	lines = append(lines, "")
	lines = append(lines, "import \"fmt\"")
	lines = append(lines, "")
	lines = append(lines, "// VTableMethod represents a vtable method index")
	lines = append(lines, "type VTableMethod int")
	lines = append(lines, "")
	lines = append(lines, "const (")

	interfaces := []struct {
		name       string
		vtableName string
	}{
		{"DataSource", "IDiaDataSourceVtbl"},
		{"Session", "IDiaSessionVtbl"},
		{"EnumSymbols", "IDiaEnumSymbolsVtbl"},
		{"EnumLineNumbers", "IDiaEnumLineNumbersVtbl"},
		{"LineNumber", "IDiaLineNumberVtbl"},
		{"SourceFile", "IDiaSourceFileVtbl"},
		{"Symbol", "IDiaSymbolVtbl"},
	}

	for _, iface := range interfaces {
		var methods []string
		generateVtableForInterface(&lines, &methods, dia2Content, iface.name, iface.vtableName)
		allMethods = append(allMethods, struct {
			iface   string
			methods []string
		}{iface.name, methods})
		lines = append(lines, "")
	}

	lines = append(lines, ")")
	lines = append(lines, "")
	lines = append(lines, "// String returns the method name")
	lines = append(lines, "func (v VTableMethod) String() string {")
	lines = append(lines, "\tif v >= 0 && int(v) < len(vtableMethodNames) {")
	lines = append(lines, "\t\treturn vtableMethodNames[v]")
	lines = append(lines, "\t}")
	lines = append(lines, "\treturn fmt.Sprintf(\"VTableMethod(%d)\", v)")
	lines = append(lines, "}")
	lines = append(lines, "")
	lines = append(lines, "// Get returns the function pointer from vtable")
	lines = append(lines, "func (v VTableMethod) Get(vtable *[200]uintptr) uintptr {")
	lines = append(lines, "\treturn vtable[v]")
	lines = append(lines, "}")
	lines = append(lines, "")
	lines = append(lines, "var vtableMethodNames = []string{")

	globalIndex := 0
	for _, am := range allMethods {
		for i, m := range am.methods {
			lines = append(lines, fmt.Sprintf("\t%d: \"%s_%s\",", globalIndex+i, am.iface, m))
		}
		globalIndex += len(am.methods)
	}
	lines = append(lines, "}")
	lines = append(lines, "")

	output := strings.Join(lines, "\n")

	cwd, _ := os.Getwd()
	outputPath := filepath.Join(cwd, "walker", "pdbex", "dia_vtable_gen.go")

	err = os.WriteFile(outputPath, []byte(output), 0o644)
	if err != nil {
		return fmt.Errorf("failed to write vtable constants: %v", err)
	}

	fmt.Println("Generated:", outputPath)
	return nil
}

func main() {
	fmt.Println("=== Syncing DIA SDK files ===")
	if err := syncDiaFiles(); err != nil {
		fmt.Println("Error syncing DIA files:", err)
		os.Exit(1)
	}

	cwd, _ := os.Getwd()
	dia2Path := filepath.Join(cwd, "walker", "pdbex", "test_dia", "dia", "include", "dia2.h")

	fmt.Println("\n=== Generating vtable constants ===")
	if err := generateVtableConstants(dia2Path); err != nil {
		fmt.Println("Error generating vtable constants:", err)
		os.Exit(1)
	}

	fmt.Println("\nDone!")
}
