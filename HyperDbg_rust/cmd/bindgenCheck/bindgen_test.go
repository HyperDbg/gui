type ExternFunction struct {
	Name       string
	File       string
	Line       int
	Params     string
	ReturnType string
	FoundInWdk bool
}

type FunctionInfo struct {
	Name       string
	Params     string
	ReturnType string
	Line       int
}

type FunctionSignature struct {
	Name       string
	File       string
	Params     string
	ReturnType string
	Line       int
	RawLine    string
}

type ExternFuncLocation struct {
	File       string
	Line       int
	FoundInWdk bool
}

type TypeMismatch struct {
	FunctionName  string
	RustFile      string
	RustLine      int
	RustSignature string
	WdkSignature  string
	WdkLine       int
	MismatchType  string
}

func TestParseNtddkFunctions(t *testing.T) {
	projectRoot := filepath.Join("..", "..")
	ntddkPath := filepath.Join(projectRoot, "cmd", "bindgenCheck", "ntddk.rs")

	content, err := os.ReadFile(ntddkPath)
	if err != nil {
		t.Fatalf("Failed to read ntddk.rs: %v", err)
	}

	functionPattern := regexp.MustCompile(`pub fn ([A-Z][a-zA-Z0-9]+)\s*\(([^)]*)\)(?:\s*->\s*([^;{]+))?`)

	lines := strings.Split(string(content), "\n")
	functions := make(map[string][]FunctionInfo)

	for i, line := range lines {
		matches := functionPattern.FindStringSubmatch(line)
		if matches == nil {
			continue
		}

		name := matches[1]
		params := strings.TrimSpace(matches[2])
		returnType := "void"
		if len(matches) > 3 && matches[3] != "" {
			returnType = strings.TrimSpace(matches[3])
		}

		prefix := ""
		if len(name) >= 2 {
			for _, p := range []string{"Ps", "Mm", "Io", "Ex", "Ob", "Ke", "Rtl", "Nt", "Dbg"} {
				if strings.HasPrefix(name, p) {
					prefix = p
					break
				}
			}
		}

		if prefix == "" {
			continue
		}

		info := FunctionInfo{
			Name:       name,
			Params:     params,
			ReturnType: returnType,
			Line:       i + 1,
		}

		functions[prefix] = append(functions[prefix], info)
	}

	for prefix, funcs := range functions {
		sort.Slice(funcs, func(i, j int) bool {
			return funcs[i].Name < funcs[j].Name
		})
		functions[prefix] = funcs
	}

	var sb strings.Builder
	sb.WriteString("// Auto-generated from ntddk.rs - DO NOT EDIT\n")
	sb.WriteString("// Generated by TestParseNtddkFunctions\n\n")

	prefixes := make([]string, 0, len(functions))
	for p := range functions {
		prefixes = append(prefixes, p)
	}
	sort.Strings(prefixes)

	for _, prefix := range prefixes {
		funcs := functions[prefix]
		sb.WriteString(fmt.Sprintf("// === %s Functions (%d) ===\n", prefix, len(funcs)))
		for _, f := range funcs {
			sb.WriteString(fmt.Sprintf("// Line %d: pub fn %s(%s)", f.Line, f.Name, f.Params))
			if f.ReturnType != "void" {
				sb.WriteString(fmt.Sprintf(" -> %s", f.ReturnType))
			}
			sb.WriteString("\n")
		}
		sb.WriteString("\n")
	}

	outputPath := filepath.Join(projectRoot, "cmd", "bindgenCheck", "functions_list.txt")
	if err := os.WriteFile(outputPath, []byte(sb.String()), 0644); err != nil {
		t.Fatalf("Failed to write functions_list.txt: %v", err)
	}

	sb.Reset()
	sb.WriteString("// Auto-generated from ntddk.rs - DO NOT EDIT\n\n")
	sb.WriteString("package bindgenCheck\n\n")
	sb.WriteString("// NtddkFunctions contains all function names from ntddk.rs\n")
	sb.WriteString("var NtddkFunctions = map[string]bool{\n")

	allNames := make([]string, 0)
	for _, funcs := range functions {
		for _, f := range funcs {
			allNames = append(allNames, f.Name)
		}
	}
	sort.Strings(allNames)

	for _, name := range allNames {
		sb.WriteString(fmt.Sprintf("\t%q: true,\n", name))
	}
	sb.WriteString("}\n")

	goOutputPath := filepath.Join(projectRoot, "cmd", "bindgenCheck", "functions_gen.go")
	if err := os.WriteFile(goOutputPath, []byte(sb.String()), 0644); err != nil {
		t.Fatalf("Failed to write functions_gen.go: %v", err)
	}

	sb.Reset()
	sb.WriteString("// Auto-generated from ntddk.rs - DO NOT EDIT\n")
	sb.WriteString("// Generated by TestParseNtddkFunctions\n\n")
	sb.WriteString("/// Ntddk function signatures for Rust code validation\n")
	sb.WriteString("/// Use this to check if a function is already defined in wdk_sys\n\n")

	for _, prefix := range prefixes {
		funcs := functions[prefix]
		sb.WriteString(fmt.Sprintf("/// === %s Functions ===\n", prefix))
		for _, f := range funcs {
			sb.WriteString(fmt.Sprintf("/// %s\n", f.Name))
		}
		sb.WriteString("\n")
	}

	rustOutputPath := filepath.Join(projectRoot, "cmd", "bindgenCheck", "functions_doc.txt")
	if err := os.WriteFile(rustOutputPath, []byte(sb.String()), 0644); err != nil {
		t.Fatalf("Failed to write functions_doc.txt: %v", err)
	}

	t.Logf("Generated files:")
	t.Logf("  - functions_list.txt: %d functions", len(allNames))
	t.Logf("  - functions_gen.go: Go map for validation")
	t.Logf("  - functions_doc.txt: Rust documentation")

	for _, prefix := range prefixes {
		t.Logf("  %s: %d functions", prefix, len(functions[prefix]))
	}
}

func TestParseTypes(t *testing.T) {
	projectRoot := filepath.Join("..", "..")
	typesPath := filepath.Join(projectRoot, "cmd", "bindgenCheck", "types.rs")

	content, err := os.ReadFile(typesPath)
	if err != nil {
		t.Fatalf("Failed to read types.rs: %v", err)
	}

	structPattern := regexp.MustCompile(`pub struct ([A-Z_][a-zA-Z0-9_]*)`)
	typePattern := regexp.MustCompile(`pub type ([A-Z_][a-zA-Z0-9_]*)\s*=`)
	enumPattern := regexp.MustCompile(`pub enum ([A-Z_][a-zA-Z0-9_]*)`)

	types := make(map[string][]string)

	for _, line := range strings.Split(string(content), "\n") {
		if matches := structPattern.FindStringSubmatch(line); matches != nil {
			types["struct"] = append(types["struct"], matches[1])
		}
		if matches := typePattern.FindStringSubmatch(line); matches != nil {
			types["type"] = append(types["type"], matches[1])
		}
		if matches := enumPattern.FindStringSubmatch(line); matches != nil {
			types["enum"] = append(types["enum"], matches[1])
		}
	}

	for kind, names := range types {
		sort.Strings(names)
		types[kind] = names
	}

	var sb strings.Builder
	sb.WriteString("// Auto-generated from types.rs - DO NOT EDIT\n\n")
	sb.WriteString("package bindgenCheck\n\n")
	sb.WriteString("// NtddkTypes contains all type names from types.rs\n")
	sb.WriteString("var NtddkTypes = map[string]bool{\n")

	seen := make(map[string]bool)
	allNames := make([]string, 0)
	for _, names := range types {
		for _, name := range names {
			if !seen[name] {
				seen[name] = true
				allNames = append(allNames, name)
			}
		}
	}
	sort.Strings(allNames)

	for _, name := range allNames {
		sb.WriteString(fmt.Sprintf("\t%q: true,\n", name))
	}
	sb.WriteString("}\n")

	outputPath := filepath.Join(projectRoot, "cmd", "bindgenCheck", "types_gen.go")
	if err := os.WriteFile(outputPath, []byte(sb.String()), 0644); err != nil {
		t.Fatalf("Failed to write types_gen.go: %v", err)
	}

	t.Logf("Generated types_gen.go:")
	t.Logf("  struct: %d", len(types["struct"]))
	t.Logf("  type: %d", len(types["type"]))
	t.Logf("  enum: %d", len(types["enum"]))
}

