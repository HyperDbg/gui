func TestScanRustExternFunctions(t *testing.T) {
	projectRoot := filepath.Join("..", "..")
	rustDriverPath := filepath.Join(projectRoot, "rust-driver", "kd", "src")
	bindgenCheckDir := filepath.Join(projectRoot, "cmd", "bindgenCheck")

	wdkFunctions := loadWdkFunctions(t, filepath.Join(bindgenCheckDir, "functions_gen.go"))

	externPattern := regexp.MustCompile(`extern\s+"C"\s*\{`)
	funcPattern := regexp.MustCompile(`fn\s+([A-Z][a-zA-Z0-9]+)\s*\(([^)]*)\)(?:\s*->\s*([^;{]+))?`)

	allExterns := make(map[string][]ExternFunction)

	err := filepath.Walk(rustDriverPath, func(path string, info os.FileInfo, err error) error {
		if err != nil {
			return err
		}

		if info.IsDir() {
			return nil
		}

		if filepath.Ext(path) != ".rs" {
			return nil
		}

		content, err := os.ReadFile(path)
		if err != nil {
			return err
		}

		lines := strings.Split(string(content), "\n")
		inExternBlock := false

		for i, line := range lines {
			if externPattern.MatchString(line) {
				inExternBlock = true
				continue
			}

			if inExternBlock {
				if strings.Contains(line, "}") {
					inExternBlock = false
					continue
				}

				if matches := funcPattern.FindStringSubmatch(line); matches != nil {
					name := matches[1]

					isWdkPrefix := false
					for _, prefix := range []string{"Ps", "Mm", "Io", "Ex", "Ob", "Ke", "Rtl", "Nt", "Dbg"} {
						if strings.HasPrefix(name, prefix) {
							isWdkPrefix = true
							break
						}
					}

					if !isWdkPrefix {
						continue
					}

					params := strings.TrimSpace(matches[2])
					returnType := "void"
					if len(matches) > 3 && matches[3] != "" {
						returnType = strings.TrimSpace(matches[3])
					}

					relPath, _ := filepath.Rel(projectRoot, path)
					extFunc := ExternFunction{
						Name:       name,
						File:       relPath,
						Line:       i + 1,
						Params:     params,
						ReturnType: returnType,
						FoundInWdk: wdkFunctions[name],
					}

					allExterns[name] = append(allExterns[name], extFunc)
				}
			}
		}

		return nil
	})

	if err != nil {
		t.Fatalf("Failed to walk rust-driver directory: %v", err)
	}

	var sb strings.Builder
	sb.WriteString("// Auto-generated Rust extern \"C\" function analysis - DO NOT EDIT\n\n")
	sb.WriteString("=== SUMMARY ===\n\n")

	missingCount := 0
	for _, funcs := range allExterns {
		if !funcs[0].FoundInWdk {
			missingCount++
		}
	}

	sb.WriteString(fmt.Sprintf("Total extern functions: %d\n", len(allExterns)))
	sb.WriteString(fmt.Sprintf("Found in WDK bindings: %d\n", len(allExterns)-missingCount))
	sb.WriteString(fmt.Sprintf("Missing from WDK bindings: %d\n\n", missingCount))

	sb.WriteString("=== FUNCTIONS MISSING FROM WDK BINDINGS ===\n\n")
	for name, funcs := range allExterns {
		if !funcs[0].FoundInWdk {
			sb.WriteString(fmt.Sprintf("%s:\n", name))
			for _, f := range funcs {
				sb.WriteString(fmt.Sprintf("  %s:%d (%s) -> %s\n", f.File, f.Line, f.Params, f.ReturnType))
			}
			sb.WriteString("\n")
		}
	}

	sb.WriteString("=== ALL EXTERN FUNCTIONS BY FILE ===\n\n")

	fileFuncs := make(map[string][]string)
	for name, funcs := range allExterns {
		for _, f := range funcs {
			fileFuncs[f.File] = append(fileFuncs[f.File], fmt.Sprintf("%s (line %d)", name, f.Line))
		}
	}

	var files []string
	for f := range fileFuncs {
		files = append(files, f)
	}
	sort.Strings(files)

	for _, file := range files {
		sb.WriteString(fmt.Sprintf("\n%s:\n", file))
		funcs := fileFuncs[file]
		sort.Strings(funcs)
		for _, f := range funcs {
			sb.WriteString(fmt.Sprintf("  - %s\n", f))
		}
	}

	sb.WriteString("\n=== ALL EXTERN FUNCTIONS BY NAME ===\n\n")

	var names []string
	for name := range allExterns {
		names = append(names, name)
	}
	sort.Strings(names)

	for _, name := range names {
		funcs := allExterns[name]
		status := "WDK"
		if !funcs[0].FoundInWdk {
			status = "MISSING"
		}
		sb.WriteString(fmt.Sprintf("%-40s %s\n", name, status))
		for _, f := range funcs {
			sb.WriteString(fmt.Sprintf("  -> %s:%d\n", f.File, f.Line))
		}
	}

	outputPath := filepath.Join(bindgenCheckDir, "rust_extern_analysis.txt")
	if err := os.WriteFile(outputPath, []byte(sb.String()), 0644); err != nil {
		t.Fatalf("Failed to write rust_extern_analysis.txt: %v", err)
	}

	sb.Reset()
	sb.WriteString("// Auto-generated - DO NOT EDIT\n\n")
	sb.WriteString("package bindgenCheck\n\n")
	sb.WriteString("// RustExternFunctions contains all extern \"C\" function declarations found in Rust code\n")
	sb.WriteString("var RustExternFunctions = map[string][]ExternFuncLocation{\n")

	for _, name := range names {
		funcs := allExterns[name]
		sb.WriteString(fmt.Sprintf("\t%q: {\n", name))
		for _, f := range funcs {
			sb.WriteString(fmt.Sprintf("\t\t{File: %q, Line: %d, FoundInWdk: %v},\n", f.File, f.Line, f.FoundInWdk))
		}
		sb.WriteString("\t},\n")
	}
	sb.WriteString("}\n")

	goOutputPath := filepath.Join(bindgenCheckDir, "rust_externs_gen.go")
	if err := os.WriteFile(goOutputPath, []byte(sb.String()), 0644); err != nil {
		t.Fatalf("Failed to write rust_externs_gen.go: %v", err)
	}

	t.Logf("Generated files:")
	t.Logf("  - rust_extern_analysis.txt: detailed analysis")
	t.Logf("  - rust_externs_gen.go: Go map for validation")
	t.Logf("  Total: %d extern functions", len(allExterns))
	t.Logf("  Missing from WDK: %d", missingCount)

	if missingCount > 0 {
		t.Logf("\nFunctions missing from WDK bindings:")
		for name, funcs := range allExterns {
			if !funcs[0].FoundInWdk {
				t.Logf("  %s (%s)", name, funcs[0].File)
			}
		}
	}
}

func loadWdkFunctions(t *testing.T, path string) map[string]bool {
	t.Helper()

	content, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("Failed to read functions_gen.go: %v", err)
	}

	functions := make(map[string]bool)
	pattern := regexp.MustCompile(`"([A-Z][a-zA-Z0-9]+)":\s*true`)

	matches := pattern.FindAllStringSubmatch(string(content), -1)
	for _, m := range matches {
		functions[m[1]] = true
	}

	return functions
}

func TestValidateTypeSignatures(t *testing.T) {
	projectRoot := filepath.Join("..", "..")
	bindgenCheckDir := filepath.Join(projectRoot, "cmd", "bindgenCheck")

	wdkSignatures := loadWdkSignatures(t, filepath.Join(bindgenCheckDir, "ntddk.rs"))

	rustExterns := scanRustExternsWithSignatures(t, projectRoot)

	var mismatches []TypeMismatch
	var notExportedFuncs []struct {
		Name  string
		Funcs []FunctionSignature
	}

	for name, funcs := range rustExterns {
		wdkSig, exists := wdkSignatures[name]
		if !exists {
			notExportedFuncs = append(notExportedFuncs, struct {
				Name  string
				Funcs []FunctionSignature
			}{Name: name, Funcs: funcs})
			continue
		}

		for _, rustFunc := range funcs {
			mismatch := compareSignatures(name, rustFunc, wdkSig)
			if mismatch != nil {
				mismatches = append(mismatches, *mismatch)
			}
		}
	}

	sort.Slice(mismatches, func(i, j int) bool {
		if mismatches[i].RustFile != mismatches[j].RustFile {
			return mismatches[i].RustFile < mismatches[j].RustFile
		}
		return mismatches[i].RustLine < mismatches[j].RustLine
	})

	sort.Slice(notExportedFuncs, func(i, j int) bool {
		return notExportedFuncs[i].Name < notExportedFuncs[j].Name
	})

	var sb strings.Builder
	sb.WriteString("// Auto-generated type signature validation report - DO NOT EDIT\n\n")
	sb.WriteString("=== TYPE SIGNATURE VALIDATION REPORT ===\n\n")

	sb.WriteString(fmt.Sprintf("Total functions checked: %d\n", len(rustExterns)))
	sb.WriteString(fmt.Sprintf("Type mismatches found: %d\n", len(mismatches)))
	sb.WriteString(fmt.Sprintf("Functions NOT exported by WDK: %d\n\n", len(notExportedFuncs)))

	if len(notExportedFuncs) > 0 {
		sb.WriteString("=== FUNCTIONS NOT EXPORTED BY WDK (need manual declaration) ===\n\n")
		sb.WriteString("// NOTE: These functions are not exported by wdk_sys bindings.\n")
		sb.WriteString("// They require manual extern \"C\" declarations with correct signatures.\n")
		sb.WriteString("// Check ntddk.rs to verify if the function exists but is not exported.\n\n")

		for _, nf := range notExportedFuncs {
			sb.WriteString(fmt.Sprintf("### %s\n", nf.Name))
			sb.WriteString("// NOT found in WDK bindings - requires manual declaration\n")
			sb.WriteString("// Locations:\n")
			for _, f := range nf.Funcs {
				sb.WriteString(fmt.Sprintf("//   - %s:%d\n", f.File, f.Line))
			}
			sb.WriteString("\n")
		}
	}

	if len(mismatches) > 0 {
		sb.WriteString("=== TYPE MISMATCHES (POTENTIAL BSOD CAUSES) ===\n\n")

		for _, m := range mismatches {
			sb.WriteString(fmt.Sprintf("FUNCTION: %s\n", m.FunctionName))
			sb.WriteString(fmt.Sprintf("  Rust File: %s:%d\n", m.RustFile, m.RustLine))
			sb.WriteString(fmt.Sprintf("  Rust Signature: fn %s\n", m.RustSignature))
			sb.WriteString(fmt.Sprintf("  WDK Signature:   fn %s (ntddk.rs:%d)\n", m.WdkSignature, m.WdkLine))
			sb.WriteString(fmt.Sprintf("  Mismatch Type: %s\n\n", m.MismatchType))
		}

		sb.WriteString("=== MISMATCHES BY FILE ===\n\n")

		fileMismatches := make(map[string][]TypeMismatch)
		for _, m := range mismatches {
			fileMismatches[m.RustFile] = append(fileMismatches[m.RustFile], m)
		}

		var files []string
		for f := range fileMismatches {
			files = append(files, f)
		}
		sort.Strings(files)

		for _, file := range files {
			sb.WriteString(fmt.Sprintf("\n%s:\n", file))
			for _, m := range fileMismatches[file] {
				sb.WriteString(fmt.Sprintf("  Line %d: %s - %s\n", m.RustLine, m.FunctionName, m.MismatchType))
			}
		}
	} else {
		sb.WriteString("No type mismatches found. All extern declarations match WDK signatures.\n")
	}

	outputPath := filepath.Join(bindgenCheckDir, "type_validation_report.txt")
	if err := os.WriteFile(outputPath, []byte(sb.String()), 0644); err != nil {
		t.Fatalf("Failed to write type_validation_report.txt: %v", err)
	}

	t.Logf("Generated type_validation_report.txt")
	t.Logf("  Total functions checked: %d", len(rustExterns))
	t.Logf("  Type mismatches found: %d", len(mismatches))
	t.Logf("  Functions NOT exported by WDK: %d", len(notExportedFuncs))

	if len(mismatches) > 0 {
		t.Logf("\n=== TYPE MISMATCHES DETECTED ===")
		for _, m := range mismatches {
			t.Logf("  %s:%d: %s - %s", m.RustFile, m.RustLine, m.FunctionName, m.MismatchType)
		}
	}

	if len(notExportedFuncs) > 0 {
		t.Logf("\n=== FUNCTIONS NOT EXPORTED BY WDK ===")
		for _, nf := range notExportedFuncs {
			t.Logf("  %s (%s)", nf.Name, nf.Funcs[0].File)
		}
	}
}

func loadWdkSignatures(t *testing.T, path string) map[string]FunctionSignature {
	t.Helper()

	content, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("Failed to read ntddk.rs: %v", err)
	}

	signatures := make(map[string]FunctionSignature)

	funcPattern := regexp.MustCompile(`pub fn ([A-Z][a-zA-Z0-9]+)\s*\(([^)]*)\)(?:\s*->\s*([^;{]+))?`)

	lines := strings.Split(string(content), "\n")
	for i, line := range lines {
		matches := funcPattern.FindStringSubmatch(line)
		if matches == nil {
			continue
		}

		name := matches[1]

		isWdkPrefix := false
		for _, prefix := range []string{"Ps", "Mm", "Io", "Ex", "Ob", "Ke", "Rtl", "Nt", "Dbg"} {
			if strings.HasPrefix(name, prefix) {
				isWdkPrefix = true
				break
			}
		}

		if !isWdkPrefix {
			continue
		}

		params := strings.TrimSpace(matches[2])
		returnType := "void"
		if len(matches) > 3 && matches[3] != "" {
			returnType = strings.TrimSpace(matches[3])
		}

		signatures[name] = FunctionSignature{
			Name:       name,
			Params:     params,
			ReturnType: returnType,
			Line:       i + 1,
			RawLine:    strings.TrimSpace(line),
		}
	}

	return signatures
}

